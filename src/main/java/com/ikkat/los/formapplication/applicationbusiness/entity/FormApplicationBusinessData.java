package com.ikkat.los.formapplication.applicationbusiness.entity;

public class FormApplicationBusinessData {
    private Long id;
    private Long idapplication;
    private String companyname;
    private String companyaddress;
    private String provinceid;
    private String division;
    private String positions;
    private int duration;
    private int numberofemployees;
    private String businessline;
    private String idregencies;
    private String iddistrict;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getIdapplication() {
        return idapplication;
    }

    public void setIdapplication(Long idapplication) {
        this.idapplication = idapplication;
    }

    public String getCompanyname() {
        return companyname;
    }

    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    public String getCompanyaddress() {
        return companyaddress;
    }

    public void setCompanyaddress(String companyaddress) {
        this.companyaddress = companyaddress;
    }

    public String getProvinceid() {
        return provinceid;
    }

    public void setProvinceid(String provinceid) {
        this.provinceid = provinceid;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public String getPositions() {
        return positions;
    }

    public void setPositions(String positions) {
        this.positions = positions;
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public int getNumberofemployees() {
        return numberofemployees;
    }

    public void setNumberofemployees(int numberofemployees) {
        this.numberofemployees = numberofemployees;
    }

    public String getBusinessline() {
        return businessline;
    }

    public void setBusinessline(String businessline) {
        this.businessline = businessline;
    }

    public String getIdregencies() {
        return idregencies;
    }

    public void setIdregencies(String idregencies) {
        this.idregencies = idregencies;
    }

    public String getIddistrict() {
        return iddistrict;
    }

    public void setIddistrict(String iddistrict) {
        this.iddistrict = iddistrict;
    }
}
